#include <iostream>
using namespace std;

int main() {
	int input1;
	int input2;

	cout << "Input1: ";
	cin >> input1;
	cout << endl;

	cout << "Input2: ";
	cin >> input2;
	cout << endl;

	cout << "Add: " << input1 + input2 << endl;
	cout << "Sub: " << input1 - input2 << endl;
	cout << "Mul: " << input1 * input2 << endl;
	cout << "Div: " << input1 / input2 << endl;
		

	return 0;
}